
public class UnaryOperator {
	public static void main(String[] args)
	{
		int ravi=23,chandu=25;
		System.out.println(ravi++);// it will increment in next print statement
		System.out.println(ravi);
		System.out.println(++chandu);// it will increment in print statement
		System.out.println(--chandu);// it will decrement the value
		System.out.println(ravi--);
		System.out.println(chandu);
		
	}

}
